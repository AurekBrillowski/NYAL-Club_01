export default function(value) {
	if(value === 1) {
		return 'NOT ASSIGNED'
	} else if(value === 2) {
		return 'Cross Country Meet'
	} else if(value === 3) {
		return 'Track Meet'
	} else if(value === 4) {
		return 'Road Race'
	} else if(value === 5) {
		return 'Trail Run'
	} else if(value === 6) {
		return "Fun Run"
	} else if(value === 7) {
		return 'Ultra'
	}
}